package basic.exercise;

public class ForExercise1 {

	// 코드의 시작점
	public static void main(String[] args) {

		
		for(int i = 0; i < 3; i+= 1) {		
			for(int g = 0; g < 2; g += 1) {
				System.out.print("ㅣ" + g + "ㅣ");
			}
			System.out.print("ㅣ"+ 2 +"ㅣ" + "\n");
		}
		
		
		

	} // end of main

} // end of class
