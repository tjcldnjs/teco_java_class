package basic.ch03;

public class Operation4_1 {

	// 코드의 시작점
	public static void main(String[] args) {

		int num1 = 0;
		// num1 = num1 + 2;
		num1 += 2;
		System.out.println(num1);

		int num2 = 0;
		// num2 = num2 - 2;
		num2 -= 2;
		System.out.println(num2);

		int num3 = 1;
		num3 *= 3;
		System.out.println(num3);

		int num4 = 10;
		num4 /= 2;
		System.out.println(num4);

		int num5 = 5;
		num5 %= 3;
		System.out.println(num5);
		
	} // end of main

} // end of class
